                <h2>Sobre a Check Point Tours</h2>
            </div>

            <p><strong>Porque você precisa de nossos serviços?</strong></p>
            <p>Acompanhado por técnicos da CHECK POINT, fluentes na sua língua, você terá o produto ou a informação desejados no menor prazo. Pesquisamos e selecionamos empresas e instituições por grupos de interesse, priorizando custo, qualidade e tradição no mercado, tornando sua estada em São Paulo muito mais prática e lucrativa.</p>

            <p><strong>Quem usa este serviço? </strong></p>
            <p>Empresas públicas e privadas, agências do governo e instituições financeiras de diversos países já usufruíram de nossos serviços, obtendo o máximo de suas visitas ao Brasil.
            </p>

            <p><strong>O que nossos programas de assistência fornecem?</strong></p>
            <p>Nosso programa completo de assistência a executivos inclui:</p>

            <p>Visitas técnicas sob medida a empresas, indústrias, fornecedoras de serviços, centros de pesquisa, instituições financeiras etc;</p>

            <p>Encontros de negócios entrevistas com pessoas-chave permitindo um contato direto com quem detém a informação desejada;</p>

            <p>Informação complementar a congressos e feiras, onde os participantes têm a oportunidade de relacionar o conhecimento adquirido com a prática através do contato com empresas atuantes no mercado abordado no evento;</p>

            <p>Pesquisa e seleção de empresas e instituições para grupos de interesse, priorizando custo, qualidade e tradição no mercado;</p>

            <p>Quaisquer consultas necessárias para otimizar seu tempo no país e sua participação em eventos;</p>
            Disponibilização de nossos consultores técnicos para encontrar o produto ou informação desejados, tornando sua viagem de negócios um sucesso.</p>

            <p><strong>Como nós trabalhamos?</strong></p>
            <p>Como exemplo, para aqueles que vêm ao Brasil para fazer contatos empresariais, primeiro fornecemos uma listagem com cinco a dez nomes de empresas promissoras, com seus números de telefones e fax, bem como pessoa de contato. Esta listagem cobre as principais (mas não necessariamente as maiores) empresas e profissionais na área de interesse. De posse destas informações, nossos clientes podem fazer seus próprios contatos ou contar com nossa ajuda. Neste caso, fornecemos um estudo completo da empresa ou do profissional. O terceiro passo consiste em preparar a visita ao Brasil. Providenciamos entrevistas, almoços e jantares de negócios, espaço físico para apresentações e negociações, intérpretes e advogados.</p>

            <p><strong>E os demais serviços de viagem?</strong></p>
            <p>Adicionalmente providenciamos reservas de hotel, alimentação, transporte e programas sócio-culturais, tais como restaurantes, museus, visita a shopping centers, além de tours e excursões aos arredores de São Paulo.</p>

            <p><strong>Qualquer agência de turismo lhe oferece alguns desses serviços, mas somente a Check Point lhe proporciona viagens de negócios sem problemas e preocupações.</strong></p>
